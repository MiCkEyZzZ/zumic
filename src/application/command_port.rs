//! Интерфейс (порт) для выполнения команд в хранилище данных.
//!
//! Этот трейт описывает операции, которые должны поддерживать команды:
//! - `execute` — выполнение команды с доступом к хранилищу данных,
//! результатом которой является значение типа `Value` или ошибка типа
//! `StoreError`.

use crate::{StorageEngine, StoreError, Value};

pub trait CommandExecute: std::fmt::Debug {
    /// Выполнить команду с доступом к хранилищу.
    ///
    /// Этот метод изменяет состояние хранилища и возвращает результат в виде
    /// значения типа `Value` или ошибку типа `StoreError`.
    fn execute(&self, store: &mut StorageEngine) -> Result<Value, StoreError>;
}
