//!
//! Примитивы базы данных и структуры данных.
//!
//! Этот модуль предоставляет основные типы данных и абстракции
//! для реализации базы данных в оперативной памяти и долговременного хранения:
//!
//! - `aof`: ведение append-only логов для долговременного хранения и восстановления данных.
//! - `aof_integrity`: проверка целостности AOF, верификация checksum и обработка повреждённых записей.
//! - `cluster`: инструменты и структуры для работы в кластерном режиме и синхронизации между нодами.
//! - `compaction`: инкрементальная и фоновая компактификация AOF для экономии дискового пространства.
//! - `memory`: внутренние структуры управления памятью, метрики и кеши.
//! - `metrics`: сбор и экспорт статистики работы базы данных и отдельных компонентов.
//! - `persistent`: поддержка персистентного хранилища, взаимодействие с файловой системой.
//! - `rebalancer`: алгоритмы перераспределения ключей между шардами для балансировки нагрузки.
//! - `recovery`: стратегии восстановления данных из AOF или snapshot'ов.
//! - `sharding`: распределение ключей по нескольким шардам для уменьшения lock contention и повышения параллельности.
//! - `slot_manager`: управление слотами/шардами, отображение ключей на слоты.
//! - `storage`: абстракции для чтения/записи данных, общий интерфейс хранилища.
//! - `store`: высокоуровневая логика работы с ключами и значениями, обработка команд.
//! - `zdb`: ядро базы данных, координация всех компонентов и обработка операций.
//! - `zdb_protocol`: протокол взаимодействия с клиентами, сериализация и десериализация команд.

