//! Подсистема модулей и плагинов Zumic.
//! Этот модуль предоставляет архитектуру для подключения
//! внешних и внутренних расширений, которые реализуют
//! дополнительные команды, хранилища, или другие функции.
//! Поддерживаются:
//!
//! - `api`: определения трейта `Module` — базового интерфейса плагина.
//! - `loader`: загрузка динамических библиотек (`.so`, `.dll`) и связывание с
//!   модулем.
//! - `plugin_manager`: менеджер плагинов: загрузка, инициализация, и
//!   маршрутизация команд.
//! - `wasm`: поддержка загрузки и исполнения WASM-плагинов через `wasmtime`.
//!
//! Все плагины могут быть одного из трёх типов:
//! - встроенные (компилируются вместе с проектом),
//! - динамические (загружаются из `.so`/`.dll`),
//! - wasm-плагины (из `.wasm`).

pub mod api;
pub mod loader;
pub mod plugin_manager;
pub mod wasm;

